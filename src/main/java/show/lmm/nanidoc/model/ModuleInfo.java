package show.lmm.nanidoc.model;

/**
 * 模块信息
 * */
public class ModuleInfo {

    /**
     * 模块名称
     * */
    private String name;

    /**
     * 模块标题
     * */
    private String title;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
